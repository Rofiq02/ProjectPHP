 -6 Bahasa Pemrograman untuk Membuat Game Android, IOS dan Desktop
 <br>
Aplikasi yang dikembangkan dalam perangka mobile memang sedang naik daun, di tahun 2016 ini bahasa pemrograman yang populer berbasis mobile adalah android. Smart phone canggih sekalipun sekarang ini menggunakan sistem operasi android. Tak heran jika programmer aplikasi mobile khususnya programmer android menjadi sangat sibuk dengan project – project yang ditawarkan oleh client. Dimulai dari project sederhana sampai dengan aplikasi rumit yang dikembangkan dari bahasa pemrograman android.</p>

<p>Saya mau jadi programmer android secara otodidak, apa yang harus saya pelajari?
<br>
Pertanyaan diatas bukanlah pertanyaan yang jarang saya dengar dan lihat, banyak keyword di google dengan menggunakan pertanyaan tersebut. Disini saya akan mencoba membantu kamu yang memang serius ingin belajar pemrograman dan ingin menjadi programmer android. Hanya dengan bermodalkan niat dan koneksi internet kamu bisa belajar di ngehh.id maupun secara otodidak.</p>

<p>Dengan mengerti menggunakan bahasa pemrograman android akan sangat membantu kamu dalam pembuatan aplikasi mobile berbasis android, membuat aplikasi game android dimana saat ini pekerjaan menjadi seorang game development mudah dalam mencari peluang untuk bersaing dan dibutuhkan perusahaan – perusahaan besar. Walaupun harga aplikasi sederhana di Indonesia khususnya begitu murah, namun belum tentu di luar sana bisa lebih mahal. Lalu darimana saya harus memulai belajar pemrograman android?</p>

<p>Dalam proses pembelajaran untuk menjadi programmer professional bahasa pemrograman android kamu harus menguasai algoritma bahasa pemrograman terlebih dahulu. Berikut kami rangkum cara dan langkah – langkah untuk menjadi programmer android.</p>

<p>Pelajari Bahasa Pemrograman Java
Bahasa pemrograman java wajib kamu kuasai sebelum beranjak untuk belajar bahasa pemrograman android itu sendiri. Dikarenakan aplikasi yang dibangun untuk android menggunakan java. Pengetahuan umum tentang bahasa pemrograman java sangat berpengaruh dalam membuat aplikasi android nantinya, misalnya class, package dan konsep java sendiri. Banyak sekali forum – forum yang membahas tentang cara belajar bahasa pemrograman java. Bisa juga langsung belajar di situs resminya java.</p>

<p>Selain bahasa pemrograman java dan javascript, khusus untuk pembuatan aplikasi game android untuk mobile kamu bisa belajar pemrograman C# (csharp). C# bisa dengan mudah dipelajari dan digunakan untuk mengembangkan atau menbangun aplikasi game android dengan bantuan aplikasi game engine, misalnya game engine Unity3D yang sudah sering kami bahas di blog ini.</p>

<p>Pengetahuan tentang XML
  <br>
Selain diwajibkan memahami tentang bahasa pemrograman java terlebih dahulu sebelum memulai membuat aplikasi android, belajar xml merupakan hal yang penting, karena xml merupakan bahasa markup yang akan banyak dipakai untuk berbagai keperluan dalam aplikasi android. Misalnya dipakai pada Webservice, Konfigurasi, Layout dan berbagai keperluan lain dalam aplikasi android. Bahasa XML tidak akan terlalu sulit untuk dipelajari baik untuk pemula. Belajar bahasa xml juga banyak di forum – forum pemrograman yang bisa kamu pelajari secara otodidak.</p>

<p>Memahami Database SQLite
  <br>
Jika membuat aplikasi Android yang menyimpan data data konfigurasi, data user dan data aplikasi didalam SQLite. Mau tidak mau kamu juga harus mengetahui sedikit banyaknya tentang SQLite. Mempelajari syntax – syntax dasar pada SQLite. Yang kamu perlukan hanya pengetahuan dasar saja, sambil jalan sambil belajar akan lebih baik dan cepat mahir untuk menjadi seorang programmer android professional.</p>

<p>Gunakan aplikasi IDE Android Studio
  <br>
Setelah siap untuk mempelajari dan membuat aplikasi android pertama kamu, gunakanlah aplikasi IDE Android Studio yang bisa kamu download secara gratis dan dikembangkan oleh pencipta bahasa pemrograman android sendiri, Android Studio disarankan oleh google jika kamu ingin membangun dan mengembangkan aplikasi mobile android dengan mudah. Banyak sekali keunggulan dari android studio dibanding dengan aplikasi lain seperti eclipse.</p>

<p>Bertanya kepada teman dan Forum Programming
  <br>
Banyak jalan menuju roma, sering bertanya disana -sini akan memudahkan kamu dalam mempelajari bahasa pemrograman populer android dengan cara otodidak dan cepat. Carilah teman yang mahir pemrograman atau bergabung dengan komunitas online yang banyak bertebaran di internet. Kamu juga bisa membeli buku – buku pemrograman android yang ada di gramedia terdekat kota kamu.</p>

<p>Try and Error
  <br>
Sering mencoba, coba, coba dan coba lagi. Bertambahnya pengalaman dalam membuat aplikasi android akan membuatmu semakin dewasa dan cepat belajar. Dengan demikian, penulis yakin kamu akan berhasil untuk menjadi programmer android dengan niat yang bersih dan tekat belajar yang kuat.</p>

