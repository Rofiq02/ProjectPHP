          <p>Tahukah, pemrograman horor itu yang seprti apa?
<br>
        “...apabila error saat compile maka keluarlah sadako dari layar monitor!”
<br>
        Jelas tidak seperti itu.
<br>
        Begini nyankoders, pemrograman horor itu maksudnya apabila kita gagal compile maka keluar sa...</p>

        <p>Diantara nyankoders, pernahkah atau mungkin sering membeli buku tentang pemrograman, apapun bahasa programannya. Pernahkah berpikir bahwah buku yang dibaca itu memiliki unsur “horor”, dalam artian sangat serius dan juga monoton dalam pembahasannya sampai-sampai malas untuk membuka lembar berikutnya. Jangan sampai malas belajar apalagi perintah pertama, iqra (bacalah). Pun sama terkadang Penulis malas kalau membaca sebuah buku pemrograman yang serius, tidak ada humornya sedikitpun (tidak seperti nyankodMagz :D), yang ada malah tegang takut error dan keluar...</p>

        <p>Maka dari itu CodePolitan akan memberikan beberapa tips untuk mengatasi agar tidak horor saat coding. Ini dia tipsnya: 
<br>
        1. Coding tidak akan pulen kalau tidak "sambil".
<br>
        Sambil makan gorengan;
<br>
        Sambil minum kopi;
<br>
        Sambil dengar musik atau murottal;
<br>
        atau sambil nonton anime seperti ini.</p>
<br>
        <p>2. Semangat biasanya muncul jika ada penyemangat. Bagi yang memiliki pasangan, mungkin coding tidak akan terlalu horor jika ditemani sama si doi(baca: istri/calon istri), apalagi pasangan yang baru.</p>

        <p>3. Kasur atau alas tidur. Ini juga penting, agar nyaman saat terjadi syntax error dan malas memperbaikinya, rebahkan badan kebelakang dan nikmati nyamannya dan empuknya alas tidur.</p>

        <p>4. Berinteraksi/ngobrol dengan runtunan kode. Mungkin ini tak lazim bagi sebagian programmer dan terkesan seperti orang gila. Tapi juga tidak sedikit mereka yang sedang coding sering ngomong sendiri. Contohnya penulis sendiri dan beberapa teman dikampus (terkadang) ngobrol sendiri dengan kode-kode yang ada dilayar monitor, ini bermaksud agar coding tetap enjoy walau terjadi kesalahan.</p>

        <p>5. Tips yang terakhir adalah hangout lewat video chat. Programmer yang sering ngomong sendiri saat coding mungkin saja sedang berbincang dengan seseorang di video chat. Ini bermaksud untuk tidak terlalu sepi dan untuk teman ngobrol. Mungkin ini bisa nyankoders coba agar coding tetap enjoy, walaupun sebagian bilang ini tidak etis layaknya programmer sejati.</p>

        <p>Itulah tips-tips untuk mengatasi agar coding tetap terasa enjoy. Mungkin sebagian nyankoders akan memilih mendengarkan murottal dibandingkan dengan yang lainnya. Semoga tips ini bermanfaat.</p>
