    <p>Aplikasi web merupakan sebuah jenis aplikasi yang menggunakan tehnologi browser untuk menjalankan sebuah aplikasi tersebut dengan di akses melalu jaringan portable. sedangkan pengertian aplikasi web yang lainya memiliki pengertian aplikasi web merupakan sebuah program yang tersimpan pada sebuah server kemudian dikirim melalui internet dan diakses dengan melalui tampilan muka browser</p>



    <p>Dari pengertian-pengertian di atas jadi dapat di simpulkan bahwa aplikasi web merupakan sebuah aplikasi yang diakses melakui web browser dengan menggunakan jaringa internet dan intranet. aplikasi web ini juga merupakan sebuah perangkat lunak atau software yang di kodekan dengan bahasa pemogramman seperti html, java script, css, ruby, python, php, dan bahasa pemogramman lainya.</p>

    <p><b>Keunggulan aplikasi berbasis web :</b>
<br>
    1.Dapat dijalankan dimanapun dan kapanpun kita berada tanpa harus melakukan penginstalan
    2.Kita tidak memerlukan sebuah lisensi ketika menggunakan aplikasi berbasis web dikarenakan lisensi merupakan sebuah tanggung jawab dari penyedia pelayanan aplikasi berbasis web
    3.Dapat di jalankan atau di gunakan di berbagai jenis sistem operasi, asalkan terhubung denga jaringan internet
    4.Dapat di akses melalui banyak media seperti komputer, laptope, notebook, handphone pintar yang sudah sesuai dengan WAP yang standart
    5.Tidak memerlukan spesifikasi yang tinggi untuk melakukan dan menggunakan aplikasi jenis web ini.</p>
    <p><b>Kekurangan aplikasi berbasis web :</b>
<br>
    1.Dibutuhkan sebuah jaringan atau koneksi internet untuk melakukan atau menggunakan aplikasi ini.
    2.Dibutuhkanya sebuah keamanan yang baik hal ini di karenakan pada jenis aplikasi berbasis web ini di jalankan secara terpusat, sehingga apabila sistem pusat mengalami down maka sistem aplikasi tidak bisa beroperasi dengan normal.
    Pengertian Aplikasi Berbasis Web</p>

