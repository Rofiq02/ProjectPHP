<br>
    1.Dapat berjalan dengan independen tanpa perlu menggunakan sebuah browser untuk mengaksesnya
    2.Tidak memerlukan sebuah koneksi internet, hal ini disebabkan karena semua file yang di perlukan untuk menjalankan sebuah aplikasi ini sudah terinstal sebelumnya.
    3.Proses lebih cepat
    4.Mudah dalam pengaturan atau modifikasi</p>
    <p><b>Kekurangan aplikasi berbasis dekstop :</b>
<br>
